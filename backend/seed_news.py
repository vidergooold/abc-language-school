async def get_or_create_category(session, name: str, slug: str, color: str) -> NewsCategory:
    result = await session.execute(select(NewsCategory).where(NewsCategory.name == name))
    cat = result.scalars().first()
    if cat:
        return cat
    cat = NewsCategory(name=name, slug=slug, color=color)
    session.add(cat)
    await session.flush()
    return cat


async def seed_news() -> None:
    await init_db()

    async with AsyncSessionLocal() as session:
        # Ensure all required categories exist
        categories: dict[str, NewsCategory] = {}
        for cat_name, (slug, color) in CATEGORIES.items():
            categories[cat_name] = await get_or_create_category(session, cat_name, slug, color)

        created = 0
        skipped = 0

        for article in ARTICLES:
            # Idempotency check: skip if title already exists
            result = await session.execute(select(News).where(News.title == article["title"]))
            if result.scalars().first():
                skipped += 1
                continue

            cat_name = article["category"]
            category = categories[cat_name]

            news = News(
                title=article["title"],
                body=article["body"],
                status=article["status"],
                category_id=category.id,
                author_id=ADMIN_USER_ID,
                date=article.get("date"),
                published_at=article.get("published_at"),
                publish_at=article.get("publish_at"),
            )
            session.add(news)
            created += 1

        await session.commit()

    print(f"\n✅ Seed завершён: создано {created} статей, пропущено {skipped} (уже существуют).")


if __name__ == "__main__":
    asyncio.run(seed_news())